fun main() {
    /*
    Augmented Assignments
    - Operasi Matematika        Augmented Assignments
    -     a = a + 10                  a += 10
    -     a = a - 10                  a -= 10
    -     a = a * 10                  a *= 10
    -     a = a / 10                  a /= 10
    -     a = a % 10                  a %= 10
     */

    var total: Int = 0;

    val barang1 = 100;
    total += barang1;

    val barang2 = 200;
    total += barang2;

    val barang3 = 300;
    total += barang3;

    println(total);

}