fun main() {
    /*

    Unary Operator
    - ++    =>  a = a + 1
    - --    =>  a = a - 1
    - -     =>  Negative
    - +     => Positive
    - !     => Boolean kebalikan

     */

    var total = 0;
    total++;
    println(total); //1

    val suhu = -5;
    println(suhu); //-5

    val sehat = true;
    println(!sehat); //false
}